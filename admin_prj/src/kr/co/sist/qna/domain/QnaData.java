package kr.co.sist.qna.domain;

public class QnaData {
	private String rnum, title,id,hiredate,qnum,answer_flag;
	
	public String getRnum() {
		return rnum;
	}

	public void setRnum(String rnum) {
		this.rnum = rnum;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getHiredate() {
		return hiredate;
	}

	public void setHiredate(String hiredate) {
		this.hiredate = hiredate;
	}

	public String getQnum() {
		return qnum;
	}

	public void setQnum(String qnum) {
		this.qnum = qnum;
	}

	public String getAnswer_flag() {
		return answer_flag;
	}

	public void setAnswer_flag(String answer_flag) {
		this.answer_flag = answer_flag;
	}




	
}
